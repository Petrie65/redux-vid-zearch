/* JS MAPS */
/* calls a function for each element in the array */

var array = [1,2,3];
array.map(function(number){
    return number * 2
});
// (3) [2, 4, 6]

var array = ["a","b","c"];
array.map(function(letter){
    return letter + "X";
});
// (3) ["aX", "bX", "cX"]

